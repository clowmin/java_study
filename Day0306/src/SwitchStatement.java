
public class SwitchStatement {

	public static void fx(){};
	public static void main(String[] args) {
		
		int a=3;
		switch(a){
		case 0: 
			System.out.println("입력값 0");
			break;
		case 1: 
			System.out.println("입력값 1");
			break;
		case 2: 
			System.out.println("입력값 2");
			break;
		case 3:
			System.out.println("입력값 3");
			break;
		default : System.out.println("해당값이 없습니다.");
		}
	}
	

}
